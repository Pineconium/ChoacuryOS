void vbe_fillrect(u32 sx, u32 sy, u32 ex, u32 ey, u32 color) {
    u32* framebuffer = (u32*)ADDRESS;
    u32 pitch_pixels = 1920;

    for (u32 y = sy; y < ey; y++) {
        u32* row_start = framebuffer + (y * pitch_pixels);
        u32* pixel_ptr = row_start + sx;
        for (u32 x = sx; x < ex; x++) {
            *pixel_ptr++ = color;
        }
    }
}

